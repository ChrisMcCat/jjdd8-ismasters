package com.infoshareacademy.parser;

public class Urls {
    private String www;

    public String getWww() {
        return www;
    }

    public void setWww(String www) {
        this.www = www;
    }

    @Override
    public String toString() {
        return "URL{" +
                "www='" + www + '\'' +
                '}';
    }
}
